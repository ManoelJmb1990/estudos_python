#String Subscripting (Indexing)

print("Hello"[1])
# Imprime a primeira letra da string "Hello", que é "H"

print("Hello"[-4])
# Imprime a segunda letra da string "Hello", que é "e"
# Usando índice negativo para acessar a segunda letra a partir do final da string



